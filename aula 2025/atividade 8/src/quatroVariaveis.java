
public class quatroVariaveis {
    public static void main(String[] args) {

        Integer A;
        Double B;
        String C;
        Boolean D;


        A = 1;
        B =  10.0;
        C =  "Hello word";
        D =  true && false;

        System.out.println("Variavel inteira : "+A);
        System.out.println("Variavel real: "+B);
        System.out.println("Variavel caractere : "+C);
        System.out.println("Variavel boolean : "+D);
    }
}
